    public void rellenarTabla(FacturasCab fac){ //Se llama al cargar el dialog para rellenar la tabla
        factura = fac;
        campoDniFactura.setText(fac.getClientes().getDnicif());
        campoFechaFactura.setText(fac.getFechafac().toString());
        campoNumFactura.setText(String.valueOf(fac.getNumfac()));
        Set <FacturasLin> lineas = fac.getFacturasLins();
        List<FacturasLin> lineasOrd = new ArrayList<>(lineas);
        lineasOrd.sort(Comparator.comparing(FacturasLin::getLinea));
        DefaultTableModel modelo = (DefaultTableModel) tablaFacturasLin.getModel();
        lineasOrd.forEach((fact) -> {
            modelo.addRow(new Object[]{fact.getId().getLineafac(), fact.getArticulos().getReferencia(), fact.getCantidad(), fact.getPrecio(), fact.getDtolinea(), fact.getIvalinea()});
        });
        campoBaseImp.setText(String.valueOf(fac.getFacturasTot().getBaseimp()));
        campoDescuento.setText(String.valueOf(fac.getFacturasTot().getImpDto()));
        campoIva.setText(String.valueOf(fac.getFacturasTot().getImpIva()));
        campoTotal.setText(String.valueOf(fac.getFacturasTot().getTotalfac()));
    }
    
    
    
