    private void botonExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonExportarActionPerformed
        JSONObject json = exportador.crearJson(factura); //Crea un objeto JSON 
        JFileChooser seleccionarRuta = new JFileChooser(); //Elegimos la ruta
        int seleccion = seleccionarRuta.showOpenDialog(null);
        if(seleccion == JFileChooser.CANCEL_OPTION){
            JOptionPane.showMessageDialog(null, "Cancelled.");
        }
        if(seleccion == JFileChooser.APPROVE_OPTION){
            if(exportador.exportarJson(json, seleccionarRuta.getSelectedFile()) == 0){ //Pasamos al exportador el json y la ruta
                JOptionPane.showMessageDialog(null, "Exported."); //Si devuelve 0 es porque no hubo fallo
            }
            else{
                JOptionPane.showMessageDialog(null, "Error trying to export."); //Si devuelve 1 hubo un fallo
            }
        }
    }//GEN-LAST:event_botonExportarActionPerformed

    private void botonExportarXmlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonExportarXmlActionPerformed
        JFileChooser seleccionarRuta = new JFileChooser();
        int seleccion = seleccionarRuta.showOpenDialog(null);
        if(seleccion == JFileChooser.CANCEL_OPTION){
            JOptionPane.showMessageDialog(null, "Cancelled.");
        }
        if(seleccion == JFileChooser.APPROVE_OPTION){
            try {
                exportador.crearXml(factura, seleccionarRuta.getSelectedFile()); //Se encarga de exportar la factura a la ruta pasados como argumento
            } catch (TransformerException | ParserConfigurationException ex) {
                JOptionPane.showMessageDialog(null, "Error trying to export.");
            }
        }
    }//GEN-LAST:event_botonExportarXmlActionPerformed

    private void botonBorrarFacturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarFacturaActionPerformed
        int seleccion = JOptionPane.showConfirmDialog(null, "Do you want to remove this bill?", "Deleting bill", JOptionPane.YES_NO_OPTION); //Se pregunta si se quiere borrar
        if(seleccion == JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, "Cancelled.");
        }
        if(seleccion == JOptionPane.YES_OPTION){
            control.borrarFactura(factura); //Si se acepta se borra
            JOptionPane.showMessageDialog(null, "Bill removed.");
            this.dispose();
        }
    }//GEN-LAST:event_botonBorrarFacturaActionPerformed

