    private void botonAgregarArtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarArtActionPerformed
         try{
            Articulos art = (Articulos) listaArticulos.getSelectedItem(); //Cogemos el articulo seleccionado del comboBox
            if(Integer.parseInt(campoCantidad.getText()) > 0 && campoCantidad.getText().length() != 0 && campoDescuento.getText().length() != 0){ //Se controla que los campos no esten vacios
                if(Double.parseDouble(campoDescuento.getText()) >= 0 && Double.parseDouble(campoDescuento.getText()) <= 100){
                    if(control.restarStock(art, Double.parseDouble(campoCantidad.getText())) == 0){ // y que queda stock suficiente

                        FacturasLin linea = new FacturasLin(); //Creamos una nueva linea de factura
                        FacturasLinId linId = new FacturasLinId(); //Con su id para relacionarla a la factura
                        linId.setLineafac(ultimaLin);
                        linId.setNumfac(facturaCab.getNumfac());
                        Set<FacturasLin> setLineas = facturaCab.getFacturasLins();
                        linea.setId(linId);
                        linea.setArticulos(art);
                        linea.setCantidad(BigDecimal.valueOf(Long.valueOf(campoCantidad.getText())));
                        linea.setPrecio(art.getPrecio());
                        linea.setDtolinea(BigDecimal.valueOf(Double.valueOf(campoDescuento.getText())));
                        linea.setIvalinea(art.getPorciva()); //Añadimos los atributos de la linea
                        setLineas.add(linea); //Añadimos la linea al set de lineas
                        facturaCab.setFacturasLins(setLineas); //Añadimos el set de lineas a la factura
                        linea.setFacturasCab(facturaCab); //Añadimos a la linea la cabecera de la factura
                        control.aniadirLinea(facturaCab); //Añadimos la linea a la BD
                        control.calcularTot(linea); //Calculamos el nuevo total de la factura
                        ultimaLin++; //Avanzamos en uno el numero de linea
                        borrarTabla(); //Borramos la tabla para eliminar registros anteriores
                        rellenarTabla(); //La rellenamos con los nuevos
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Insufficient stock.");
                        ultimaLin++; //Se debe avanzar la linea o da error
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Discount must be between 0 and 100");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Check amount and discount fields.");
            }
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Insert a correct amount.");
        }
    }//GEN-LAST:event_botonAgregarArtActionPerformed

    private void botonBorrarArtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonBorrarArtActionPerformed
        indiceFacturas = tablaFacturas.getSelectedRow(); //Se coge el indice del registro seleccioando
        
        if(indiceFacturas != -1){ //Si se ha seleccionado un registro
            
            FacturasLin linea = (FacturasLin) tablaFacturas.getValueAt(indiceFacturas, 0); //Cogemos el objeto FacturasLin del registro seleccionado
            Set<FacturasLin> lineas = facturaCab.getFacturasLins();
            lineas.remove(linea);
            facturaCab.setFacturasLins(lineas);
            control.borrarLinea(linea);
            
            borrarTabla();
            rellenarTabla();
        }
        else{
            JOptionPane.showMessageDialog(null, "Select a row.");
        }
    }//GEN-LAST:event_botonBorrarArtActionPerformed

