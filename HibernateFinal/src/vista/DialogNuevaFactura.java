    public void setFacturaCab(FacturasCab factura){ //Se ejecuta al abrir el dialog y se le pasa la factura en cuestion para trabajar con ella
        control = new ControlDatos();
        facturaCab = factura; 
        campoNumFac.setText(String.valueOf(factura.getNumfac()));
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yy");
        campoFechaFac.setText(formato.format(factura.getFechafac()));
        campoDniCliente.setText(factura.getClientes().getDnicif());
        List<Articulos> articulos = control.recibirArticulos();
        articulos.forEach((art) -> {
            listaArticulos.addItem(art);
        });
    }
    
    private void rellenarTabla(){ //Rellena la tabla con sus registros correspondientes
        DefaultTableModel modelo = (DefaultTableModel) tablaFacturas.getModel();
        Set<FacturasLin> linea = facturaCab.getFacturasLins();
        List<FacturasLin> lineasOrdenadas = new ArrayList<>(linea);
        lineasOrdenadas.sort(Comparator.comparing(FacturasLin::getLinea));
        lineasOrdenadas.forEach((lin) -> {
            modelo.addRow(new Object[]{lin, lin.getArticulos(),lin.getCantidad(), lin.getPrecio(), lin.getDtolinea(), lin.getIvalinea()});
        });
    }
    
    private void borrarTabla(){ //Quita los registros de la tabla
        DefaultTableModel modelo = (DefaultTableModel) tablaFacturas.getModel();
        while(tablaFacturas.getRowCount() != 0){
            modelo.removeRow(0);
        }
    }
    
